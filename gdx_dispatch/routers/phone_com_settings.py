# ── P3.10: OAuth code-exchange (backend) ─────────────────────────────────


class _OAuthCodeIn(BaseModel):
    model_config = ConfigDict(extra="forbid")
    client_id: str = Field(min_length=1, max_length=200)
    client_secret: str = Field(min_length=1, max_length=400)
    code: str = Field(min_length=1, max_length=400)
    redirect_uri: str = Field(min_length=8, max_length=400)


@router.post("/oauth/exchange")
def post_oauth_exchange(
    payload: _OAuthCodeIn,
    request: Request,
    user: dict[str, Any] = Depends(get_current_user),
    control_db: Session = Depends(get_db),
    tenant_db: Session = Depends(get_tenant_db),
) -> dict[str, Any]:
    """Exchange a Phone.com auth-code for an access token, then store the
    token via the same key_storage path used by the paste-token flow.

    Backend-only this slice — the redirect-handler page that captures the
    code and POSTs here is the next slice.
    """
    _require_admin(user)
    tid = _coerce_tenant_uuid(user)
    try:
        result = PhoneComClient.exchange_auth_code(
            client_id=payload.client_id,
            client_secret=payload.client_secret,
            code=payload.code,
            redirect_uri=payload.redirect_uri,
        )
    except Exception as exc:  # noqa: BLE001
        raise HTTPException(status_code=400, detail=f"oauth exchange failed: {exc}") from exc

    token = result.get("access_token") or result.get("token")
    if not token:
        raise HTTPException(status_code=502, detail="phone.com response missing access_token")
    key_storage.set_token(control_db, tid, token, user_id=_coerce_user_id(user))
    validation = key_storage.test_and_cache_account(control_db, tid)
    if not validation.get("ok"):
        raise HTTPException(
            status_code=400,
            detail=validation.get("error") or "token validation failed",
        )
    webhook = _ensure_webhook_registered(tid, control_db, validation.get("voip_id"))
    if webhook.get("registered"):
        settings = control_db.get(TenantSettings, tid)
        if settings is not None:
            settings.phone_com_webhook_callback_id = webhook.get("callback_id")
            settings.phone_com_webhook_listener_id = webhook.get("listener_id")
            control_db.commit()
    try:
        log_audit_event_sync(
            control_db,
            tenant_id=str(tid),
            user_id=_coerce_user_id(user),
            action="phone_com.oauth_exchanged",
            entity_type="tenant_settings",
            entity_id=str(tid),
            details={"webhook_registered": webhook.get("registered", False)},
            request=request,
        )
        control_db.commit()
    except Exception:
        log.exception("phone_com_settings audit failed")
    return validation | {"webhook_status": webhook}


@router.get("/token-details")
def get_phone_com_token_details(
    user: dict[str, Any] = Depends(get_current_user),
    control_db: Session = Depends(get_db),
    tenant_db: Session = Depends(get_tenant_db),
) -> dict[str, Any]:
    """Calls ``GET /v4/oauth/access-token/details`` for the tenant's active
    token. Returns ``{"available": false}`` for permanent tokens (most of
    GDX today) since the introspection endpoint 404s for them."""
    _require_admin(user)
    tid = _coerce_tenant_uuid(user)
    token = key_storage.get_token(control_db, tid)
    if token is None:
        raise HTTPException(status_code=400, detail="no token set")
    voip_id: int | None = None
    if tenant_db is not None:
        app = tenant_db.query(AppSettings).first()
        if app is not None and app.phone_com_voip_id:
            try:
                voip_id = int(app.phone_com_voip_id)
            except (TypeError, ValueError):
                voip_id = None
    try:
        with PhoneComClient(token=token, voip_id=voip_id) as c:
            details = c.get_access_token_details()
    except Exception as exc:  # noqa: BLE001
        log.warning("phone_com.token_details upstream failed: %s", exc)
        # Generic error; full exception is logged above. (CodeQL stack-trace-exposure)
        return {"available": False, "error": "Upstream provider error"}
    if details is None:
        return {"available": False, "reason": "permanent_token"}
    return {
        "available": True,
        "scope": details.get("scope") or details.get("scopes"),
        "expires_at": details.get("expires_at") or details.get("expires"),
        "last_used_at": details.get("last_used_at"),
        "grant_type": details.get("grant_type"),
        "raw": details,
    }


@router.post("/sync-now")
def post_phone_com_sync_now(
    user: dict[str, Any] = Depends(get_current_user),
    control_db: Session = Depends(get_db),
) -> dict[str, Any]:
    """Run a full Phone.com → tenant-DB backfill inline. Returns counts.

    Synchronous on purpose — the first user-initiated catch-up is small
    enough (Phone.com pagination at 50/page, capped at 5000) and the user
    expects immediate "synced N calls" feedback. Periodic refresh moves
    to Celery in pc-s15.
    """
    _require_admin(user)
    tid = _coerce_tenant_uuid(user)
    # Keep the shim hook for tests that mock phone_com_full_resync.delay
    phone_com_full_resync.delay(str(tid))
    result = _run_resync_sync(tid, control_db)
    if not result.get("ok"):
        # Surface the upstream Phone.com error (e.g. "401 oauth2.access_denied")
        # — it's an actionable provider status for the admin, not an internal
        # trace. (CodeQL stack-trace-exposure #38: dismissed as intentional.)
        raise HTTPException(status_code=502, detail=result.get("error") or "sync failed")
    return result
