@router.get("/api/billing/usage")
def billing_usage(user: dict = Depends(get_current_user), db: Session = Depends(get_db)):
    now = datetime.now(UTC)
    first_of_month = now.replace(day=1, hour=0, minute=0, second=0, microsecond=0)
    try:
        jobs_count = db.execute(
            select(func.count()).select_from(Job).where(Job.created_at >= first_of_month, Job.deleted_at.is_(None))
        ).scalar() or 0
        inv_count = db.execute(
            select(func.count()).select_from(Invoice).where(Invoice.created_at >= first_of_month, Invoice.deleted_at.is_(None))
        ).scalar() or 0
    except Exception:
        log.exception("billing_usage_query_failed")
        jobs_count, inv_count = 0, 0
    return {"current_month": {"jobs": jobs_count, "invoices": inv_count, "users": 0}}


